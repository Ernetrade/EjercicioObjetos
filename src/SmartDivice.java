public class SmartDivice {

    String software;
    double pulgadas;
    double version;
    boolean cargador;

    public SmartDivice(){

    }

    public SmartDivice(String software, double pulgadas, double version, boolean cargador) {
        this.software = software;
        this.pulgadas = pulgadas;
        this.version = version;
        this.cargador = cargador;
    }

    
}
